def compute_per_dish_errors(predictions, metadata):
    """Compute per-dish absolute errors and percentage errors.

    Returns list of dicts with keys: dish_id, errors (list of 5), pct_errors, labels, preds.
    """
    results = []
    for dish_id, preds in predictions.items():
        if dish_id not in metadata:
            continue
        labels = metadata[dish_id]["labels"]
        errors = [abs(preds[i] - labels[i]) for i in range(5)]
        pct_errors = [100 * errors[i] / max(abs(labels[i]), 1e-6) for i in range(5)]
        results.append({
            "dish_id": dish_id,
            "preds": preds,
            "labels": labels,
            "errors": errors,
            "pct_errors": pct_errors,
        })
    return results


def compute_group_mae(dish_results, metadata, group_fn):
    """Compute MAE% per group for a set of dish results.

    Returns {group_name: [mae_pct per nutrient]}.
    """
    groups = {}
    for r in dish_results:
        dish_id = r["dish_id"]
        if dish_id not in metadata:
            continue
        group = group_fn(metadata[dish_id])
        if group not in groups:
            groups[group] = {"errors": [], "labels": []}
        groups[group]["errors"].append(r["errors"])
        groups[group]["labels"].append(r["labels"])

    group_mae_pct = {}
    for group, data in groups.items():
        errors = np.array(data["errors"])
        labels = np.array(data["labels"])
        mae = errors.mean(axis=0)
        mean_gt = np.abs(labels).mean(axis=0)
        mean_gt = np.maximum(mean_gt, 1e-6)
        group_mae_pct[group] = (100 * mae / mean_gt).tolist()

    return group_mae_pct


