def plot_improvement_heatmap(exp_maes_pct, output_dir):
    """Heatmap showing % improvement of each experiment vs the first (baseline)."""
    if len(exp_maes_pct) < 2:
        return

    exp_names = list(exp_maes_pct.keys())
    baseline_name = exp_names[0]
    baseline = np.array(exp_maes_pct[baseline_name])

    compare_names = exp_names[1:]
    n_compare = len(compare_names)

    # Compute % change: negative = improvement, positive = worse
    improvements = np.zeros((n_compare, len(LABEL_NAMES)))
    for i, name in enumerate(compare_names):
        vals = np.array(exp_maes_pct[name])
        improvements[i] = ((vals - baseline) / np.maximum(baseline, 1e-6)) * 100

    fig, ax = plt.subplots(figsize=(10, max(3, n_compare * 0.8 + 2)))
    im = ax.imshow(improvements, cmap="RdYlGn_r", aspect="auto",
                   vmin=-max(abs(improvements.min()), abs(improvements.max())),
                   vmax=max(abs(improvements.min()), abs(improvements.max())))

    ax.set_xticks(range(len(LABEL_NAMES)))
    ax.set_xticklabels([n.capitalize() for n in LABEL_NAMES])
    ax.set_yticks(range(n_compare))
    ax.set_yticklabels(compare_names)

    # Annotate cells
    for i in range(n_compare):
        for j in range(len(LABEL_NAMES)):
            val = improvements[i, j]
            sign = "+" if val > 0 else ""
            ax.text(j, i, f"{sign}{val:.1f}%", ha="center", va="center",
                    fontsize=10, color="black")

    ax.set_title(f"% Change in MAE% vs Baseline ({baseline_name})\n(green = better, red = worse)")
    fig.colorbar(im, ax=ax, label="% Change")
    fig.tight_layout()
    fig.savefig(os.path.join(output_dir, "comparison_improvement_heatmap.png"), dpi=150)
    plt.close(fig)


def print_summary_table(exp_maes, exp_maes_pct):
    """Print a summary table to console."""
    print("\n" + "=" * 80)
    print("EXPERIMENT COMPARISON SUMMARY")
    print("=" * 80)

    # Header
    header = f"  {'Experiment':<25}"
    for name in LABEL_NAMES:
        header += f" {name[:7]:>9}"
    header += f" {'Overall':>9}"
    print(header)
    print("  " + "-" * (25 + 10 * 6))

    # MAE% rows
    print("  MAE%:")
    for exp_name, values in exp_maes_pct.items():
        row = f"    {exp_name:<23}"
        for v in values:
            row += f" {v:>8.1f}%"
        row += f" {np.mean(values):>8.1f}%"
        print(row)

    # MAE rows
    print("\n  MAE:")
    for exp_name, values in exp_maes.items():
        row = f"    {exp_name:<23}"
        for v in values:
            row += f" {v:>9.2f}"
        row += f" {np.mean(values):>9.2f}"
        print(row)

    print("=" * 80)


