"""
Compare multiple saved experiments and generate presentation-ready plots.

Auto-discovers experiments under models/<name>/ and generates comparison
plots from training histories and per-dish predictions.

Each experiment directory should contain:
  - plots/training_history.json  (always present after training)
  - predictions.csv              (generated by test.py with OUTPUT_CSV set)

Usage:
    python3 scripts/compare_experiments.py
    python3 scripts/compare_experiments.py --models_dir models --data_dir data
    python3 scripts/compare_experiments.py --output_dir comparison_plots
"""

