def setup_style():
    """Set up consistent plot styling."""
    plt.rcParams.update({
        "figure.facecolor": "white",
        "axes.facecolor": "white",
        "axes.grid": True,
        "grid.alpha": 0.3,
        "axes.spines.top": False,
        "axes.spines.right": False,
        "font.size": 11,
        "axes.titlesize": 13,
        "axes.labelsize": 11,
    })


def plot_mae_bar(exp_maes, output_dir, metric="mae", ylabel="MAE", filename=None):
    """Grouped bar chart comparing MAE or MAE% across experiments."""
    if not exp_maes:
        return

    exp_names = list(exp_maes.keys())
    n_exp = len(exp_names)
    n_nutrients = len(LABEL_NAMES)

    x = np.arange(n_nutrients)
    width = 0.8 / n_exp

    fig, ax = plt.subplots(figsize=(12, 6))
    for i, name in enumerate(exp_names):
        values = exp_maes[name]
        offset = (i - n_exp / 2 + 0.5) * width
        bars = ax.bar(x + offset, values, width * 0.9, label=name, color=COLORS[i % len(COLORS)])
        # Add value labels on bars
        for bar, val in zip(bars, values):
            ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height(),
                    f"{val:.1f}", ha="center", va="bottom", fontsize=8)

    ax.set_xlabel("Nutrient")
    ax.set_ylabel(ylabel)
    unit = "%" if "pct" in metric.lower() or "%" in ylabel else ""
    ax.set_title(f"Per-Nutrient {ylabel} Comparison Across Experiments")
    ax.set_xticks(x)
    ax.set_xticklabels([n.capitalize() for n in LABEL_NAMES])
    ax.legend(loc="upper right")
    fig.tight_layout()

    fname = filename or f"comparison_{metric}_bar.png"
    fig.savefig(os.path.join(output_dir, fname), dpi=150)
    plt.close(fig)


def plot_training_curves(histories, output_dir):
    """Overlay training curves (val MAE per nutrient) for all experiments."""
    if not histories:
        return

    fig, axes = plt.subplots(2, 3, figsize=(18, 10))
    axes = axes.flatten()

    exp_names = list(histories.keys())

    # Per-nutrient subplots
    for ni, nutrient in enumerate(LABEL_NAMES):
        ax = axes[ni]
        key = f"val_mae_pct_{nutrient}"
        for ei, name in enumerate(exp_names):
            h = histories[name]
            if key in h:
                ax.plot(h["epoch"], h[key], "-", linewidth=1.5,
                        color=COLORS[ei % len(COLORS)], label=name)
        ax.set_title(f"{nutrient.capitalize()} MAE%")
        ax.set_xlabel("Epoch")
        ax.set_ylabel("MAE %")
        if ni == 0:
            ax.legend(fontsize=8)

    # Overall val loss in the 6th subplot
    ax = axes[5]
    for ei, name in enumerate(exp_names):
        h = histories[name]
        if "val_loss" in h:
            ax.plot(h["epoch"], h["val_loss"], "-", linewidth=1.5,
                    color=COLORS[ei % len(COLORS)], label=name)
    ax.set_title("Overall Val MAE")
    ax.set_xlabel("Epoch")
    ax.set_ylabel("MAE")
    ax.legend(fontsize=8)

    fig.suptitle("Training Curves Comparison", fontsize=15, y=1.01)
    fig.tight_layout()
    fig.savefig(os.path.join(output_dir, "comparison_training_curves.png"), dpi=150, bbox_inches="tight")
    plt.close(fig)


def plot_radar(exp_maes_pct, output_dir):
    """Radar/spider chart comparing normalized performance across nutrients."""
    if not exp_maes_pct:
        return

    exp_names = list(exp_maes_pct.keys())
    categories = [n.capitalize() for n in LABEL_NAMES]
    n_cats = len(categories)

    angles = [n / float(n_cats) * 2 * math.pi for n in range(n_cats)]
    angles += angles[:1]  # close the polygon

    fig, ax = plt.subplots(figsize=(8, 8), subplot_kw=dict(polar=True))

    for ei, name in enumerate(exp_names):
        values = list(exp_maes_pct[name])  # copy to avoid mutating original
        values += values[:1]
        ax.plot(angles, values, "o-", linewidth=2, color=COLORS[ei % len(COLORS)], label=name)
        ax.fill(angles, values, alpha=0.1, color=COLORS[ei % len(COLORS)])

    ax.set_xticks(angles[:-1])
    ax.set_xticklabels(categories)
    ax.set_title("MAE% by Nutrient (lower is better)", pad=20)
    ax.legend(loc="upper right", bbox_to_anchor=(1.3, 1.1))
    fig.tight_layout()
    fig.savefig(os.path.join(output_dir, "comparison_radar.png"), dpi=150, bbox_inches="tight")
    plt.close(fig)


def plot_grouped_bar(group_data, exp_names, output_dir, title, filename):
    """Grouped bar chart for per-group MAE% (e.g., by ingredient or density).

    group_data: {exp_name: {group_name: [mae_pct per nutrient]}}
    """
    if not group_data:
        return

    # Find common groups across all experiments, sorted by frequency
    all_groups = set()
    for exp in group_data.values():
        all_groups.update(exp.keys())

    # Sort groups, keep "Other" and "Unknown" at end
    special = {"Other", "Unknown"}
    sorted_groups = sorted(g for g in all_groups if g not in special)
    sorted_groups.extend(sorted(g for g in all_groups if g in special))

    # Use overall MAE% (mean across nutrients) for the bar
    n_groups = len(sorted_groups)
    n_exp = len(exp_names)
    x = np.arange(n_groups)
    width = 0.8 / max(n_exp, 1)

    fig, ax = plt.subplots(figsize=(max(14, n_groups * 1.2), 7))
    for ei, name in enumerate(exp_names):
        values = []
        for group in sorted_groups:
            if group in group_data.get(name, {}):
                vals = group_data[name][group]
                values.append(np.mean(vals))
            else:
                values.append(0)
        offset = (ei - n_exp / 2 + 0.5) * width
        ax.bar(x + offset, values, width * 0.9, label=name, color=COLORS[ei % len(COLORS)])

    ax.set_xlabel("Category")
    ax.set_ylabel("Overall MAE %")
    ax.set_title(title)
    ax.set_xticks(x)
    ax.set_xticklabels(sorted_groups, rotation=45, ha="right")
    ax.legend()
    fig.tight_layout()
    fig.savefig(os.path.join(output_dir, filename), dpi=150)
    plt.close(fig)


def plot_scatter(all_dish_results, output_dir):
    """Predicted vs Actual scatter plots, one per nutrient with subplots per experiment."""
    if not all_dish_results:
        return

    exp_names = list(all_dish_results.keys())
    n_exp = len(exp_names)

    for ni, nutrient in enumerate(LABEL_NAMES):
        n_cols = min(n_exp, 3)
        n_rows = math.ceil(n_exp / n_cols)
        fig, axes = plt.subplots(n_rows, n_cols, figsize=(6 * n_cols, 5 * n_rows), squeeze=False)

        for ei, name in enumerate(exp_names):
            ax = axes[ei // n_cols][ei % n_cols]
            results = all_dish_results[name]
            actuals = [r["labels"][ni] for r in results]
            predicted = [r["preds"][ni] for r in results]

            ax.scatter(actuals, predicted, alpha=0.3, s=15, color=COLORS[ei % len(COLORS)])

            # y=x reference line
            lo = min(min(actuals), min(predicted))
            hi = max(max(actuals), max(predicted))
            ax.plot([lo, hi], [lo, hi], "k--", linewidth=1, alpha=0.5)

            ax.set_xlabel(f"Actual {nutrient} ({LABEL_UNITS[nutrient]})")
            ax.set_ylabel(f"Predicted {nutrient} ({LABEL_UNITS[nutrient]})")
            ax.set_title(f"{name}")
            ax.set_aspect("equal", adjustable="datalim")

        # Hide unused subplots
        for ei in range(n_exp, n_rows * n_cols):
            axes[ei // n_cols][ei % n_cols].axis("off")

        fig.suptitle(f"Predicted vs Actual: {nutrient.capitalize()}", fontsize=14)
        fig.tight_layout()
        fig.savefig(os.path.join(output_dir, f"comparison_scatter_{nutrient}.png"), dpi=150)
        plt.close(fig)


