def main():
    parser = argparse.ArgumentParser(
        description="Compare multiple Nutrition5k experiments with visualization plots."
    )
    parser.add_argument("--models_dir", default="./models",
                        help="Directory containing experiment subdirectories (default: ./models)")
    parser.add_argument("--data_dir", default="./data",
                        help="Path to Nutrition5k data directory (default: ./data)")
    parser.add_argument("--output_dir", default="./comparison_plots",
                        help="Directory to save comparison plots (default: ./comparison_plots)")
    args = parser.parse_args()

    setup_style()
    os.makedirs(args.output_dir, exist_ok=True)

    # --- Discover experiments ---
    print("=== Discovering experiments ===")
    experiments = discover_experiments(args.models_dir)
    print(f"  Found {len(experiments)} experiments: {', '.join(experiments.keys())}")
    print()

    # --- Load training histories ---
    print("=== Loading training histories ===")
    histories = {}
    for name, exp_dir in experiments.items():
        h = load_training_history(exp_dir)
        if h:
            histories[name] = h
            n_epochs = len(h.get("epoch", []))
            best_val = min(h.get("val_loss", [float("inf")]))
            print(f"  {name}: {n_epochs} epochs, best val MAE: {best_val:.2f}")
        else:
            print(f"  {name}: no training_history.json found, skipping training curves")
    print()

    # --- Load predictions ---
    print("=== Loading predictions ===")
    all_predictions = {}
    for name, exp_dir in experiments.items():
        preds = load_predictions(exp_dir)
        if preds:
            all_predictions[name] = preds
            print(f"  {name}: {len(preds)} dish predictions loaded")
        else:
            print(f"  {name}: no predictions.csv found (run test.py with OUTPUT_CSV to generate)")
    print()

    # --- Load metadata ---
    print("=== Loading metadata ===")
    metadata = load_metadata(args.data_dir)
    print(f"  Loaded {len(metadata)} dishes from metadata")
    print()

    # --- Compute metrics from training histories (best epoch) ---
    exp_maes = {}
    exp_maes_pct = {}
    for name, h in histories.items():
        # Find best epoch by overall val MAE
        best_idx = int(np.argmin(h.get("val_loss", [float("inf")])))
        mae_vals = [h[f"val_mae_{n}"][best_idx] for n in LABEL_NAMES]
        mae_pct_vals = [h[f"val_mae_pct_{n}"][best_idx] for n in LABEL_NAMES]
        exp_maes[name] = mae_vals
        exp_maes_pct[name] = mae_pct_vals

    # If we have predictions + metadata, compute test-set metrics instead (more accurate)
    exp_maes_test = {}
    exp_maes_pct_test = {}
    all_dish_results = {}
    for name, preds in all_predictions.items():
        results = compute_per_dish_errors(preds, metadata)
        if results:
            all_dish_results[name] = results
            errors = np.array([r["errors"] for r in results])
            labels = np.array([r["labels"] for r in results])
            mae = errors.mean(axis=0)
            mean_gt = np.abs(labels).mean(axis=0)
            mean_gt = np.maximum(mean_gt, 1e-6)
            mae_pct = (100 * mae / mean_gt)
            exp_maes_test[name] = mae.tolist()
            exp_maes_pct_test[name] = mae_pct.tolist()

    # Prefer test-set metrics if available, fall back to val metrics
    final_maes = exp_maes_test if exp_maes_test else exp_maes
    final_maes_pct = exp_maes_pct_test if exp_maes_pct_test else exp_maes_pct

