def discover_experiments(models_dir):
    """Find all experiment directories under models_dir/."""
    experiments = {}
    if not os.path.isdir(models_dir):
        print(f"Error: models directory '{models_dir}' not found.")
        sys.exit(1)

    for name in sorted(os.listdir(models_dir)):
        exp_dir = os.path.join(models_dir, name)
        if os.path.isdir(exp_dir):
            experiments[name] = exp_dir

    if not experiments:
        print(f"Error: no experiment directories found in '{models_dir}'.")
        sys.exit(1)

    return experiments


def load_training_history(exp_dir):
    """Load training_history.json from an experiment directory."""
    path = os.path.join(exp_dir, "plots", "training_history.json")
    if not os.path.exists(path):
        return None
    with open(path, "r") as f:
        return json.load(f)


def load_predictions(exp_dir):
    """Load predictions CSV from an experiment. Returns {dish_id: [cal, mass, fat, carbs, protein]}."""
    # Try predictions.csv first, then any predictions*.csv
    candidates = [os.path.join(exp_dir, "predictions.csv")]
    candidates.extend(sorted(glob.glob(os.path.join(exp_dir, "predictions*.csv"))))

    for path in candidates:
        if os.path.exists(path):
            preds = {}
            with open(path, "r") as f:
                reader = csv.reader(f)
                for row in reader:
                    if not row or not row[0].startswith("dish_"):
                        continue
                    dish_id = row[0].strip()
                    try:
                        values = [float(row[i]) for i in range(1, 6)]
                        preds[dish_id] = values
                    except (ValueError, IndexError):
                        continue
            if preds:
                return preds
    return None


def load_metadata(data_dir):
    """Load dish metadata. Returns {dish_id: {'labels': [...], 'ingredients': [...]}}."""
    metadata = {}
    for csv_rel in METADATA_CSVS:
        csv_path = os.path.join(data_dir, csv_rel)
        if not os.path.exists(csv_path):
            continue
        with open(csv_path, "r") as f:
            for line in f:
                fields = line.strip().split(",")
                if not fields or not fields[0].startswith("dish_"):
                    continue
                dish_id = fields[0]
                try:
                    labels = [float(fields[i]) for i in range(1, 6)]
                except (ValueError, IndexError):
                    continue

                # Parse ingredients
                ingredients = []
                ingr_fields = fields[6:]
                for i in range(0, len(ingr_fields) - 6, 7):
                    try:
                        ingredients.append({
                            "name": ingr_fields[i + 1],
                            "grams": float(ingr_fields[i + 2]),
                            "cal": float(ingr_fields[i + 3]),
                        })
                    except (ValueError, IndexError):
                        continue

                metadata[dish_id] = {
                    "labels": labels,
                    "ingredients": ingredients,
                }
    return metadata


def get_dominant_ingredient(dish_meta):
    """Return the name of the heaviest ingredient by mass."""
    ingredients = dish_meta.get("ingredients", [])
    if not ingredients:
        return "Unknown"
    return max(ingredients, key=lambda x: x["grams"])["name"]


def get_calorie_density_bin(dish_meta):
    """Categorize dish by calorie density (cal/gram)."""
    labels = dish_meta["labels"]
    cal, mass = labels[0], labels[1]
    if mass <= 0:
        return "Unknown"
    density = cal / mass
    if density < 1.0:
        return "Low (<1 cal/g)"
    elif density <= 2.0:
        return "Medium (1-2 cal/g)"
    else:
        return "High (>2 cal/g)"


def get_portion_size_bin(dish_meta):
    """Categorize dish by portion size (mass in grams)."""
    mass = dish_meta["labels"][1]
    if mass < 150:
        return "Small (<150g)"
    elif mass <= 350:
        return "Medium (150-350g)"
    else:
        return "Large (>350g)"


